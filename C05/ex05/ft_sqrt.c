#include <stdio.h>

int	ft_sqrt(int nb)
{
	int sq;

	sq = 1;
	if (nb > 46340 * 46340)
		return (0);
	while (sq * sq < nb)
		sq++;
	if ((sq * sq) == nb)
		return (sq);
	return (0);
}
